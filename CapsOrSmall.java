
public class CapsOrSmall {

	public static void main(String[] args)
	{		
		
		char ch= 'W';
		{ 
		  
		    if (ch >= 'A' && ch <= 'Z') 
		        System.out.println("caps");
		  
		    else if (ch >= 'a' && ch <= 'z') 
		    	 System.out.println("small"); 
		              
		  
		    else
		    	 System.out.println("other");
		              
		} 
		

	}

}
